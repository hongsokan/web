var express = require('express');
var app = express();
var fs = require('fs');
var bodyParser = require('body-parser');
var compression = require('compression');
// index ('/') 라우터
var indexRouter = require('./routes/index')
// create, update, delete, read 기능들의 라우터
var topicRouter = require('./routes/topic');

// 미들웨어 사용
// 1. 정적 파일 제공하기 위한 express 기본 미들웨어 함수
// public이라는 이름의 디렉토리에 포함된 파일들 제공
app.use(express.static('public'));
// 2. parse application/x-www-form-urlencoded
app.use(bodyParser.urlencoded({ extended: false }));
// 3. compress all responses
app.use(compression());

// 미들웨어 작성
app.use(function (request, response, next) {
  fs.readdir('./data', function (error, filelist) {
    request.list = filelist;
    next();
  });
})

// '/'으로 시작하는 주소에 미들웨어 indexRouter 적용
app.use('/', indexRouter);
// '/topic' 으로 시작하는 주소들에 미들웨어 topicRouter 적용
app.use('/topic', topicRouter);

// 에러처리
app.use(function(req, res, next) {
  res.status(404).send('Sorry cant find the page!');
});
 
app.use(function (err, req, res, next) {
  console.error(err.stack)
  res.status(500).send('Something broke!')
});

app.listen(3000, function () {
  console.log('Example app listening on port 3000!')
});



